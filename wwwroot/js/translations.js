    init() {
        // Apply current language on page load with a small delay to ensure DOM is ready
        setTimeout(() => {
            this.applyLanguage();
        }, 100);
        
        // Listen for language changes
        document.addEventListener('languageChanged', (e) => {
            this.currentLanguage = e.detail.language;
            this.applyLanguage();
        });
    }
    
    translate(key, fallback = key) {
        const translation = this.translations[this.currentLanguage];
        if (translation && translation[key]) {
            return translation[key];
        }
        return fallback;
    }
    
    applyLanguage() {
        console.log('Applying language:', this.currentLanguage);
        
        // Find all elements with data-translate attribute
        const elements = document.querySelectorAll('[data-translate]');
        console.log('Found', elements.length, 'elements to translate');
        console.log('Elements found:', elements);
        
        // Update all elements with data-translate attribute
        elements.forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = this.translate(key);
            
            console.log('Translating element:', key, '->', translation, 'Element:', element);
            
            if (element.tagName === 'INPUT' && (element.type === 'text' || element.type === 'email' || element.type === 'password')) {
                element.placeholder = translation;
            } else if (element.tagName === 'INPUT' && element.type === 'submit') {
                element.value = translation;
            } else if (element.tagName === 'TITLE') {
                element.textContent = translation;
            } else {
                // For all other elements, just update the text content
                element.textContent = translation;
            }
        });
        
        // Update page title
        const titleElement = document.querySelector('title');
        if (titleElement && titleElement.getAttribute('data-translate')) {
            titleElement.textContent = this.translate(titleElement.getAttribute('data-translate'));
        }
        
        // Update document title
        const pageTitle = document.querySelector('h1[data-translate]');
        if (pageTitle) {
            document.title = this.translate(pageTitle.getAttribute('data-translate'));
        }
    }
    
    setLanguage(language) {
        this.currentLanguage = language;
        localStorage.setItem('selectedLanguage', language);
        
        // Dispatch custom event
        document.dispatchEvent(new CustomEvent('languageChanged', {
            detail: { language: language }
        }));
        
        // Apply language immediately
        this.applyLanguage();
    }
    
    getCurrentLanguage() {
        return this.currentLanguage;
    }
    
    getAvailableLanguages() {
        return Object.keys(this.translations);
    }
